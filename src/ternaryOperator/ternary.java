package ternaryOperator;

public class ternary {
    // (condition) ? True : False
    // (condition statemnt) ? if true : if false
    public static void main(String[] args) {

        String isStudent = (false) ? "Yes" : "No";
        System.out.println(isStudent);

        boolean isincludes= (isStudent.contains("Y")) ? true : false;
        System.out.println(isincludes);

    }

}
